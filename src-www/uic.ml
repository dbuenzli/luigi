open React


type ui_link = Brc.link
let ui_link ?id ?classes ?title l =
  let href = S.const (D.Ui_link.href l) in
  let text = S.const [`Txt (D.Ui_link.text l)] in
  let classes =
    let base = [ Br.str "button"; Br.str "link"; Br.str "ui-link" ] in
    match classes with
    | None -> S.const base
    | Some classes -> S.map (fun l -> List.rev_append base l) classes
  in
  Brc.link ?title ~classes ~href text

let ui_link_el = Brc.link_el
let ui_link_actuate = Brc.link_actuate

